    private void btnautenticarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnautenticarActionPerformed
       
        String pass = String.valueOf(Password.getPassword());
        String sql = "Insert usuarios(fecha,usuario,clave) values(?,?,?)";

        try {
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, ((JTextField) Date.getDateEditor().getUiComponent()).getText());
            pst.setString(2,Txtusuario.getText());
            pst.setString(3, pass);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Registro Correcto");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de registro");
        }
        
    }//GEN-LAST:event_btnautenticarActionPerformed

