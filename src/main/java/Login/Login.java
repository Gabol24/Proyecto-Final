   public void ValidarUsuario() {
        int Resultado = 0;
        
        try {
            String usuario = txtusuario.getText();
            String pass = String.valueOf(password.getPassword());
            String sql = "SELECT * FROM usuarios WHERE usuario = '" + usuario + "'and clave ='" + pass + "'";
            Statement st = conectar.createStatement();
            ResultSet rs = st.executeQuery(sql);
            if (rs.next()) {
                Resultado = 1;

            }
            if (Resultado == 1) {
                Sistema Abrir = new Sistema();
                Abrir.setVisible(true);// abrir Sistema
                this.dispose();// cerrar Sistema

            }else{
                JOptionPane.showMessageDialog(null, "Revise usuario y contraseña");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Revise el error");
        }
    }
   
